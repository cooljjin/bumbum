'use client';

import React, { useState, useEffect, useRef } from 'react';
import { motion, AnimatePresence } from 'framer-motion';

interface PerformanceMetrics {
  fps: number;
  frameTime: number;
  memoryUsage: number;
  renderCalls: number;
  triangles: number;
  points: number;
  lines: number;
  timestamp: number;
}

interface PerformanceHistory {
  fps: number[];
  memoryUsage: number[];
  frameTime: number[];
  timestamps: number[];
}

interface OptimizationSuggestion {
  id: string;
  type: 'warning' | 'info' | 'critical';
  title: string;
  description: string;
  action?: string;
  impact: 'low' | 'medium' | 'high';
  autoFixable?: boolean;
  fixFunction?: () => void;
}

interface PerformanceDashboardProps {
  enabled?: boolean;
  position?: 'top-right' | 'top-left' | 'bottom-right' | 'bottom-left';
  compact?: boolean;
  showHistory?: boolean;
  onOptimizationSuggestion?: (suggestion: OptimizationSuggestion) => void;
}

export const PerformanceDashboard: React.FC<PerformanceDashboardProps> = ({
  enabled = true,
  position = 'top-right',
  compact = false,
  showHistory = true,
  onOptimizationSuggestion
}) => {
  const [metrics, setMetrics] = useState<PerformanceMetrics>({
    fps: 0,
    frameTime: 0,
    memoryUsage: 0,
    renderCalls: 0,
    triangles: 0,
    points: 0,
    lines: 0,
    timestamp: Date.now()
  });

  const [history, setHistory] = useState<PerformanceHistory>({
    fps: [],
    memoryUsage: [],
    frameTime: [],
    timestamps: []
  });

  const [suggestions, setSuggestions] = useState<OptimizationSuggestion[]>([]);
  const [isExpanded, setIsExpanded] = useState(false);
  const [isVisible, setIsVisible] = useState(true);

  const historyRef = useRef<PerformanceHistory>(history);
  const maxHistorySize = 100; // 100Í∞ú Îç∞Ïù¥ÌÑ∞ Ìè¨Ïù∏Ìä∏ Ïú†ÏßÄ

  // ÏÑ±Îä• Î©îÌä∏Î¶≠ ÏóÖÎç∞Ïù¥Ìä∏
  useEffect(() => {
    if (!enabled) return;

    const updateMetrics = () => {
      const memoryInfo = (performance as any).memory;
      const currentMemory = memoryInfo ? Math.round(memoryInfo.usedJSHeapSize / 1024 / 1024) : 0;
      
      // Í∞ÑÎã®Ìïú FPS Í≥ÑÏÇ∞ (Ïã§Ï†úÎ°úÎäî Îçî Ï†ïÍµêÌïú Í≥ÑÏÇ∞Ïù¥ ÌïÑÏöî)
      const now = performance.now();
      const fps = Math.round(1000 / (now - (metrics.timestamp || now)));
      
      const newMetrics: PerformanceMetrics = {
        fps: Math.min(fps, 120), // ÏµúÎåÄ 120fpsÎ°ú Ï†úÌïú
        frameTime: Math.round(now - (metrics.timestamp || now)),
        memoryUsage: currentMemory,
        renderCalls: 0, // Ïã§Ï†ú Î†åÎçîÎßÅ ÌÜµÍ≥ÑÎäî Three.jsÏóêÏÑú Í∞ÄÏ†∏ÏôÄÏïº Ìï®
        triangles: 0,
        points: 0,
        lines: 0,
        timestamp: now
      };

      setMetrics(newMetrics);

      // ÌûàÏä§ÌÜ†Î¶¨ ÏóÖÎç∞Ïù¥Ìä∏
      const newHistory = {
        fps: [...historyRef.current.fps, newMetrics.fps].slice(-maxHistorySize),
        memoryUsage: [...historyRef.current.memoryUsage, newMetrics.memoryUsage].slice(-maxHistorySize),
        frameTime: [...historyRef.current.frameTime, newMetrics.frameTime].slice(-maxHistorySize),
        timestamps: [...historyRef.current.timestamps, now].slice(-maxHistorySize)
      };

      historyRef.current = newHistory;
      setHistory(newHistory);

      // ÏÑ±Îä• ÏµúÏ†ÅÌôî Ï†úÏïà ÏÉùÏÑ±
      generateOptimizationSuggestions(newMetrics, newHistory);
    };

    const interval = setInterval(updateMetrics, 1000); // 1Ï¥àÎßàÎã§ ÏóÖÎç∞Ïù¥Ìä∏
    return () => clearInterval(interval);
  }, [enabled, metrics.timestamp]);

  // ÏÑ±Îä• ÏµúÏ†ÅÌôî Ï†úÏïà ÏÉùÏÑ±
  const generateOptimizationSuggestions = (
    currentMetrics: PerformanceMetrics,
    currentHistory: PerformanceHistory
  ) => {
    const newSuggestions: OptimizationSuggestion[] = [];

    // FPS Í∏∞Î∞ò Ï†úÏïà
    if (currentMetrics.fps < 30) {
      newSuggestions.push({
        id: 'low-fps',
        type: 'critical',
        title: 'ÎÇÆÏùÄ FPS Í∞êÏßÄ',
        description: `ÌòÑÏû¨ FPSÍ∞Ä ${currentMetrics.fps}Î°ú ÏÑ±Îä•Ïù¥ Ï†ÄÌïòÎêòÍ≥† ÏûàÏäµÎãàÎã§.`,
        action: 'Î†åÎçîÎßÅ ÌíàÏßàÏùÑ ÎÇÆÏ∂îÍ±∞ÎÇò Î∂àÌïÑÏöîÌïú Í∞ùÏ≤¥Î•º Ï†úÍ±∞ÌïòÏÑ∏Ïöî.',
        impact: 'high'
      });
    } else if (currentMetrics.fps < 50) {
      newSuggestions.push({
        id: 'medium-fps',
        type: 'warning',
        title: 'FPS Í∞úÏÑ† ÌïÑÏöî',
        description: `ÌòÑÏû¨ FPSÍ∞Ä ${currentMetrics.fps}ÏûÖÎãàÎã§.`,
        action: 'ÏùºÎ∂Ä Ìö®Í≥ºÎ•º ÎπÑÌôúÏÑ±ÌôîÌïòÍ±∞ÎÇò LODÎ•º Ï°∞Ï†ïÌïòÏÑ∏Ïöî.',
        impact: 'medium'
      });
    }

    // Î©îÎ™®Î¶¨ ÏÇ¨Ïö©Îüâ Í∏∞Î∞ò Ï†úÏïà
    if (currentMetrics.memoryUsage > 150) {
      newSuggestions.push({
        id: 'high-memory',
        type: 'critical',
        title: 'ÎÜíÏùÄ Î©îÎ™®Î¶¨ ÏÇ¨Ïö©Îüâ',
        description: `Î©îÎ™®Î¶¨ ÏÇ¨Ïö©ÎüâÏù¥ ${currentMetrics.memoryUsage}MBÏûÖÎãàÎã§.`,
        action: 'ÏÇ¨Ïö©ÌïòÏßÄ ÏïäÎäî ÌÖçÏä§Ï≤òÎÇò Î™®Îç∏ÏùÑ Ï†ïÎ¶¨ÌïòÏÑ∏Ïöî.',
        impact: 'high'
      });
    } else if (currentMetrics.memoryUsage > 100) {
      newSuggestions.push({
        id: 'medium-memory',
        type: 'warning',
        title: 'Î©îÎ™®Î¶¨ ÏÇ¨Ïö©Îüâ Ï£ºÏùò',
        description: `Î©îÎ™®Î¶¨ ÏÇ¨Ïö©ÎüâÏù¥ ${currentMetrics.memoryUsage}MBÏûÖÎãàÎã§.`,
        action: 'Î©îÎ™®Î¶¨ ÏÇ¨Ïö©ÎüâÏùÑ Î™®ÎãàÌÑ∞ÎßÅÌïòÏÑ∏Ïöî.',
        impact: 'medium'
      });
    }

    // ÌîÑÎ†àÏûÑ ÌÉÄÏûÑ Í∏∞Î∞ò Ï†úÏïà
    if (currentMetrics.frameTime > 33) { // 30fps ÎØ∏Îßå
      newSuggestions.push({
        id: 'high-frame-time',
        type: 'warning',
        title: 'ÎÜíÏùÄ ÌîÑÎ†àÏûÑ ÌÉÄÏûÑ',
        description: `ÌîÑÎ†àÏûÑ ÌÉÄÏûÑÏù¥ ${currentMetrics.frameTime}msÏûÖÎãàÎã§.`,
        action: 'Î†åÎçîÎßÅ ÏµúÏ†ÅÌôîÎ•º Í≥†Î†§ÌïòÏÑ∏Ïöî.',
        impact: 'medium'
      });
    }

    // Î©îÎ™®Î¶¨ ÎàÑÏàò Í∞êÏßÄ (Î©îÎ™®Î¶¨ ÏÇ¨Ïö©ÎüâÏù¥ ÏßÄÏÜçÏ†ÅÏúºÎ°ú Ï¶ùÍ∞ÄÌïòÎäî Í≤ΩÏö∞)
    if (currentHistory.memoryUsage.length >= 10) {
      const recentMemory = currentHistory.memoryUsage.slice(-10);
      const isIncreasing = recentMemory.every((val, i) => i === 0 || val >= recentMemory[i - 1]);
      const increaseRate = recentMemory[recentMemory.length - 1] - recentMemory[0];
      
      if (isIncreasing && increaseRate > 10) {
        newSuggestions.push({
          id: 'memory-leak',
          type: 'critical',
          title: 'Î©îÎ™®Î¶¨ ÎàÑÏàò ÏùòÏã¨',
          description: `Î©îÎ™®Î¶¨ ÏÇ¨Ïö©ÎüâÏù¥ ÏßÄÏÜçÏ†ÅÏúºÎ°ú Ï¶ùÍ∞ÄÌïòÍ≥† ÏûàÏäµÎãàÎã§.`,
          action: 'Î©îÎ™®Î¶¨ ÎàÑÏàòÎ•º ÌôïÏù∏ÌïòÍ≥† Ï†ïÎ¶¨ÌïòÏÑ∏Ïöî.',
          impact: 'high'
        });
      }
    }

    setSuggestions(newSuggestions);
    
    // Î∂ÄÎ™® Ïª¥Ìè¨ÎÑåÌä∏Ïóê Ï†úÏïà Ï†ÑÎã¨
    if (onOptimizationSuggestion && newSuggestions.length > 0) {
      newSuggestions.forEach(suggestion => {
        onOptimizationSuggestion(suggestion);
      });
    }
  };

  // ÏÑ±Îä• ÏÉÅÌÉúÏóê Îî∞Î•∏ ÏÉâÏÉÅ Í≤∞Ï†ï
  const getPerformanceColor = (fps: number) => {
    if (fps >= 50) return '#10b981'; // green
    if (fps >= 30) return '#f59e0b'; // yellow
    return '#ef4444'; // red
  };

  // ÏÑ±Îä• ÏÉÅÌÉúÏóê Îî∞Î•∏ ÏïÑÏù¥ÏΩò
  const getPerformanceIcon = (fps: number) => {
    if (fps >= 50) return 'üü¢';
    if (fps >= 30) return 'üü°';
    return 'üî¥';
  };

  // ÏúÑÏπòÏóê Îî∞Î•∏ Ïä§ÌÉÄÏùº
  const getPositionStyle = () => {
    const baseStyle = {
      position: 'fixed' as const,
      zIndex: 9999,
      fontFamily: 'monospace',
      fontSize: '12px',
      color: '#ffffff',
      backgroundColor: 'rgba(0, 0, 0, 0.8)',
      borderRadius: '8px',
      padding: '8px',
      backdropFilter: 'blur(10px)',
      border: '1px solid rgba(255, 255, 255, 0.1)'
    };

    switch (position) {
      case 'top-right':
        return { ...baseStyle, top: '20px', right: '20px' };
      case 'top-left':
        return { ...baseStyle, top: '20px', left: '20px' };
      case 'bottom-right':
        return { ...baseStyle, bottom: '20px', right: '20px' };
      case 'bottom-left':
        return { ...baseStyle, bottom: '20px', left: '20px' };
      default:
        return { ...baseStyle, top: '20px', right: '20px' };
    }
  };

  if (!enabled || !isVisible) return null;

  return (
    <div style={getPositionStyle()}>
      {/* Ïª¥Ìå©Ìä∏ Î™®Îìú */}
      {compact ? (
        <div 
          style={{ cursor: 'pointer', display: 'flex', alignItems: 'center', gap: '8px' }}
          onClick={() => setIsExpanded(!isExpanded)}
        >
          <span>{getPerformanceIcon(metrics.fps)}</span>
          <span style={{ color: getPerformanceColor(metrics.fps) }}>
            {metrics.fps} FPS
          </span>
          {suggestions.length > 0 && (
            <span style={{ color: '#ef4444' }}>‚ö†Ô∏è</span>
          )}
        </div>
      ) : (
        /* ÌôïÏû• Î™®Îìú */
        <div>
          {/* Ìó§Îçî */}
          <div style={{ 
            display: 'flex', 
            justifyContent: 'space-between', 
            alignItems: 'center',
            marginBottom: '8px',
            borderBottom: '1px solid rgba(255, 255, 255, 0.2)',
            paddingBottom: '4px'
          }}>
            <span style={{ fontWeight: 'bold' }}>ÏÑ±Îä• Î™®ÎãàÌÑ∞</span>
            <div style={{ display: 'flex', gap: '4px' }}>
              <button
                onClick={() => setIsExpanded(!isExpanded)}
                style={{
                  background: 'none',
                  border: 'none',
                  color: '#ffffff',
                  cursor: 'pointer',
                  fontSize: '12px'
                }}
              >
                {isExpanded ? '‚ñº' : '‚ñ∂'}
              </button>
              <button
                onClick={() => setIsVisible(false)}
                style={{
                  background: 'none',
                  border: 'none',
                  color: '#ffffff',
                  cursor: 'pointer',
                  fontSize: '12px'
                }}
              >
                ‚úï
              </button>
            </div>
          </div>

          {/* Í∏∞Î≥∏ Î©îÌä∏Î¶≠ */}
          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '4px', marginBottom: '8px' }}>
            <div>
              <span style={{ color: getPerformanceColor(metrics.fps) }}>
                {getPerformanceIcon(metrics.fps)} {metrics.fps} FPS
              </span>
            </div>
            <div>
              <span style={{ color: '#60a5fa' }}>
                üíæ {metrics.memoryUsage}MB
              </span>
            </div>
            <div>
              <span style={{ color: '#a78bfa' }}>
                ‚è±Ô∏è {metrics.frameTime}ms
              </span>
            </div>
            <div>
              <span style={{ color: '#fbbf24' }}>
                üî∫ {metrics.triangles}
              </span>
            </div>
          </div>

          {/* ÌôïÏû•Îêú Ï†ïÎ≥¥ */}
          <AnimatePresence>
            {isExpanded && (
              <motion.div
                initial={{ height: 0, opacity: 0 }}
                animate={{ height: 'auto', opacity: 1 }}
                exit={{ height: 0, opacity: 0 }}
                style={{ overflow: 'hidden' }}
              >
                {/* ÌûàÏä§ÌÜ†Î¶¨ Ï∞®Ìä∏ (Í∞ÑÎã®Ìïú ÌÖçÏä§Ìä∏ Í∏∞Î∞ò) */}
                {showHistory && (
                  <div style={{ marginBottom: '8px' }}>
                    <div style={{ fontSize: '10px', marginBottom: '4px' }}>FPS ÌûàÏä§ÌÜ†Î¶¨:</div>
                    <div style={{ 
                      display: 'flex', 
                      gap: '1px', 
                      height: '20px',
                      alignItems: 'end'
                    }}>
                      {history.fps.slice(-20).map((fps, index) => (
                        <div
                          key={index}
                          style={{
                            width: '3px',
                            height: `${Math.max(2, (fps / 60) * 20)}px`,
                            backgroundColor: getPerformanceColor(fps),
                            borderRadius: '1px'
                          }}
                        />
                      ))}
                    </div>
                  </div>
                )}

                {/* ÏµúÏ†ÅÌôî Ï†úÏïà */}
                {suggestions.length > 0 && (
                  <div>
                    <div style={{ fontSize: '10px', marginBottom: '4px' }}>ÏµúÏ†ÅÌôî Ï†úÏïà:</div>
                    {suggestions.map((suggestion) => (
                      <div
                        key={suggestion.id}
                        style={{
                          fontSize: '10px',
                          padding: '4px',
                          marginBottom: '2px',
                          backgroundColor: suggestion.type === 'critical' ? 'rgba(239, 68, 68, 0.2)' : 
                                         suggestion.type === 'warning' ? 'rgba(245, 158, 11, 0.2)' : 
                                         'rgba(59, 130, 246, 0.2)',
                          borderRadius: '4px',
                          border: `1px solid ${suggestion.type === 'critical' ? '#ef4444' : 
                                          suggestion.type === 'warning' ? '#f59e0b' : '#3b82f6'}`
                        }}
                      >
                        <div style={{ fontWeight: 'bold' }}>{suggestion.title}</div>
                        <div>{suggestion.description}</div>
                        {suggestion.action && (
                          <div style={{ fontSize: '9px', opacity: 0.8 }}>{suggestion.action}</div>
                        )}
                      </div>
                    ))}
                  </div>
                )}
              </motion.div>
            )}
          </AnimatePresence>
        </div>
      )}
    </div>
  );
};

export default PerformanceDashboard;
